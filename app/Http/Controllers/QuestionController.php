    public function edit($id)
    {
        $questions = Question::where('id', '=', $id)->get();
        return view('admin.datas.edit', compact('questions'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request,Question $question)
    {
        $data = request()->validate([
            'question' => 'required',
        ]);

        $question -> update($request->all());

        if(!is_null($question)) {
            return redirect('/standarts/' .$question->standart_id. '/edit')->with('success', 'Berhasil edit Pertanyaan');
        }
        else {
            return back()->with("error", "Proses Gagal");
        }
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $data = Question::findOrFail($id);
        $data->delete();

        return redirect()->back()->with('success','Berhasil Menghapus data');
    }
